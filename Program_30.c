// WAP to print sum of each values generated in each iteration
#include<stdio.h>
void main()
{
    int temp = 0;
    for(int i =1; i<=10; i = i+1)
    {
        temp = temp + i; // Adding value of i generated in each iteration and storing the sum in temp variable.
    }
}

